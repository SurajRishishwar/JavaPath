class DataTypes{
    public static void main(String a[]){
        byte b = 125;
        short s = 453;
        int n = 5456;
        long l = 45678l; // followed by l in the end.

        float f = 78.3f; // followed by f in the end.
        double d = 233.9033;

        char c = 's'; // we can not use " ".

        boolean bb = true; // we can not use 0 and 1 inplace of true and false.

        System.out.println(bb);
    }
}