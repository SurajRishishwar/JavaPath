class Loops{
    public static void main(String ar[]){
    
        // While Loop
        int i=1;
        while(/*condition*/i <= 4){
            System.out.println("Its While");
            i++;
        }

        // for Loop
        for(int j=0;j<4;j++){
            System.out.println("For Loop");
        }

        // Do While
        int k=1;
        do{
            System.out.println("Do While");
            k++;
        }while(k<5);


    }
}